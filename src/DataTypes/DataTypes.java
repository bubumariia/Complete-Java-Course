package DataTypes;

public class DataTypes {
    public static void main(String[] args){
        boolean firstBoolean = false;
        if(firstBoolean == true){
            System.out.println("Boolean is True");
        }
        System.out.println("Boolean is False");

        char firstChar = 'k';
        System.out.println("First character is "+firstChar+ " " + (int)firstChar);

        byte firstByte = 127;
        System.out.println("First byte " + firstByte );

        short firstShort = 17783;
        System.out.println("First short  is " + firstShort);

        int firstInt = 32000;
        System.out.println(firstInt);



    }
}
